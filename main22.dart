void main() {
  // Write a program to print numbers from 1 to 10:
  for (int y = 1; y <= 10; y++) {
    print(' number is $y');
  }
  // Write a program to calculate the sum of the first 10 natural numbers:
  int sum = 0;
  for (int x = 1; x <= 10; x++) {
    print('value is $x');
    sum = sum + x;
    print("now sum is $sum");
  }
  print('now the sum of first ten digits is $sum');
  print('end!');
}
