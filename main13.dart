//Dart Relational & Equality Operator:
void main() {
  var x = 10;
  var y = 20;
  // <,>,==,<=,>=,!= (operators):
  // relational operators <,>,<=,>=:
  // equality operators <=,>=,==,!=:
  bool result1, result2, result3, result4, result5, result6;
  result1 = x > y;
  result2 = x < y;
  result3 = x == y;
  result4 = x <= y;
  result5 = x >= y;
  result6 = x != y;
  print(result1);
  print(result2);
  print(result3);
  print(result4);
  print(result5);
  print(result6);
}
